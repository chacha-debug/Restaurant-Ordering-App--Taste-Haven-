    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        // TODO add your handling code here:
        
    String otpInput = jTextField3.getText().trim();
    String newPassword = new String(jPasswordField1.getPassword());
    String email = jTextField2.getText().trim();

    if (otpInput.isEmpty() || newPassword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Fill all fields.");
        return;
    }

    if (!otpInput.equals(generatedOTP)) {
        JOptionPane.showMessageDialog(this, "Incorrect OTP.");
        return;
    }

    try (Connection conn = ConnectionProvider.getCon()) {
        PreparedStatement stmt = conn.prepareStatement("UPDATE users SET password_hash = SHA2(?, 256) WHERE email = ?");
        stmt.setString(1, newPassword);
        stmt.setString(2, email);
        int updated = stmt.executeUpdate();

        if (updated > 0) {
            JOptionPane.showMessageDialog(this, "Password updated.");
            this.dispose(); // Close this window
            new LoginForm().setVisible(true); // Show login again
        } else {
            JOptionPane.showMessageDialog(this, "Reset failed.");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
        
    }//GEN-LAST:event_button2ActionPerformed


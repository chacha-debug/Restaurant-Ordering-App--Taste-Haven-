    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
       String email = jTextField2.getText().trim();
    if (email.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter your email.");
        return;
    }

    try (Connection conn = ConnectionProvider.getCon()) {
        PreparedStatement stmt = conn.prepareStatement("SELECT * FROM users WHERE email = ?");
        stmt.setString(1, email);
        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            generatedOTP = String.valueOf(new Random().nextInt(900000) + 100000);
            label2.setText("OTP: " + generatedOTP);

            // Hide OTP after 10 seconds
            if (hideOtpTimer != null && hideOtpTimer.isRunning()) hideOtpTimer.stop();
            hideOtpTimer = new Timer(10000, new ActionListener() {
                public void actionPerformed(ActionEvent e) {
                    label2.setText("OTP hidden for security.");
                    hideOtpTimer.stop();
                }
            });
            hideOtpTimer.setRepeats(false);
            hideOtpTimer.start();

        } else {
            JOptionPane.showMessageDialog(this, "Email not found.");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    }
        
    }//GEN-LAST:event_button1ActionPerformed

